package DataStructure.Exercises;

public class Entry{
    private int k;
    private String v;

    public Entry(int k, String v){
        this.k = k;
        this.v = v;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
         this.v = v;
    }

    public int getK() {
        return k;
    }
}
